package CalculadoraIMC;

public class ArrayBinario {
    public static void main(String[] args) {
        int[] numeros=new int[3];
        numeros[0]=2;
        numeros[1]=5;
        numeros[2]=4;
        numeros[3]=9;

        for (int i=0;i<numeros.length;i++){
            System.out.println(numeros);
        }
    }
}
